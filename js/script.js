const cardme1 = document.getElementById("cardme1");

cardme1.addEventListener("mouseenter", () => {
    cardme1.style.transform = "translateY(-10px)";
    cardme1.style.transition = "transform 0.3s ease";
});

cardme1.addEventListener("mouseleave", () => {
    cardme1.style.transform = "translateY(0px)";
});

